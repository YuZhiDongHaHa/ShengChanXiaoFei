package qqqqq;

import java.math.BigDecimal;

public class BaoZi {
	private String name;
	private BigDecimal price;
	public BaoZi(String name, BigDecimal price) {
		super();
		this.name = name;
		this.price = price;
	}
	public BaoZi() {
		super();
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public BigDecimal getPrice() {
		return price;
	}
	public void setPrice(BigDecimal price) {
		this.price = price;
	}
	
	
	
	
}
